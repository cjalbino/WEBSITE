						<div class="text">The name of the Province was derived from the Malay word Jambangan, meaning a pot or place of flowers. Historically, Zamboanga was the capital of the Moro Province, which comprised of five (5) districts, namely: Cotabato, Davao, Sulu, Lanao and Zamboanga. In 1940, these districts became individual provinces. Zamboanga City became the capital of the Zamboanga Province. Soon after the Second World War, the provincial capital was transferred to Dipolog. After that, Molave was created as the provincial capital of the Province of Zamboanga by virtue of RA 286 dated June 16, 1948. Zamboanga del Sur was carved out from the former Zamboanga Province that encompassed the entire peninsula in Southwestern Mindanao on September 17, 1952 by virtue of Republic Act 711. As the 52nd province of the Republic, it originally consisted of 11 towns, which later expanded into 42 municipalities and one city-Pagadian, its capital.</div>
